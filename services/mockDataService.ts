const parseCSV = (text: string): string[][] => {
  if (!text) return [];
  const lines = text.trim().split('\n').filter(line => line.trim() !== '');
  return lines.map(line => {
    const row: string[] = [];
    let inQuote = false;
    let token = '';
    for (let i = 0; i < line.length; i++) {
      const char = line[i];
      if (char === '"') {
        inQuote = !inQuote;
      } else if (char === ',' && !inQuote) {
        row.push(token);
        token = '';
      } else {
        token += char;
      }
    }
    row.push(token);
    return row;
  });
};

const parsePastQuestions = (csv: string, explanationCsv?: string): Question[] => {
  const rows = parseCSV(csv);
  const explanationRows = explanationCsv ? parseCSV(explanationCsv) : [];
  const explanationMap = new Map<string, string>();
  
  explanationRows.forEach(row => {
    if (row.length >= 2) {
      explanationMap.set(row[0], row[1]);
    }
  });

  // Filter out any potential header row if it exists and looks like a header
  const dataRows = rows.filter(r => r[0] !== 'ID' && r[0] !== '問1' && r.length > 5 || r[0].startsWith('問')); 
  
  return dataRows.map(row => {
    const id = row[0];
    const typeStr = row[1];
    const type = typeStr === '4択' ? QuestionType.FOUR_CHOICE : QuestionType.TRUE_FALSE;
    const category = row[2];
    const text = row[3];
    const options = type === QuestionType.FOUR_CHOICE 
      ? [row[4], row[5], row[6], row[7]].filter(Boolean)
      : undefined;
    // Map answer: For 〇×, 1=True, 2=False. For 4択, 1-4.
    const correctAnswer = parseInt(row[8], 10) || 1;
    // Use explanation from separate CSV if available, otherwise fallback to row[9] or default
    const explanation = explanationMap.get(id) || row[9] || "解説準備中";

    return { id, type, category, text, options, correctAnswer, explanation };
  });
};

const parsePredictedQuestions = (csv: string, explanationCsv?: string): Question[] => {
  const rows = parseCSV(csv).slice(1); // Skip header
  const explanationRows = explanationCsv ? parseCSV(explanationCsv).slice(1) : [];
  const explanationMap = new Map<string, string>();

  explanationRows.forEach(row => {
    if (row.length >= 2) {
      explanationMap.set(row[0], row[1]);
    }
  });

  return rows.map(row => {
    const id = row[0];
    const category = row[1];
    const fullText = row[2] || "";
    
    const fourChoiceRegex = /^(.*?)[\s　]?1\.(.*?)[\s　]?2\.(.*?)[\s　]?3\.(.*?)[\s　]?4\.(.*)$/;
    const match = fullText.match(fourChoiceRegex);

    const correctAnswer = parseInt(row[3], 10) || 1;
    // Use explanation from separate CSV if available, otherwise fallback to row[4] or default
    const explanation = explanationMap.get(id) || row[4] || "解説準備中";

    if (match) {
      return {
        id: `P${id}`,
        type: QuestionType.FOUR_CHOICE,
        category,
        text: match[1].trim(),
        options: [match[2].trim(), match[3].trim(), match[4].trim(), match[5].trim()],
        correctAnswer,
        explanation
      };
    } else {
      return {
        id: `P${id}`,
        type: QuestionType.TRUE_FALSE,
        category,
        text: fullText,
        options: undefined,
        correctAnswer,
        explanation
      };
    }
  });
};

// filter: For PAST mode, it represents 'examId' (e.g. '46').
//         For PREDICTED mode, it represents 'category' (e.g. '法規').
export const getQuestions = async (mode: 'past' | 'predicted', filter?: string): Promise<Question[]> => {
  await new Promise(resolve => setTimeout(resolve, 300));
  
  let questions: Question[] = [];

  if (mode === 'past') {
    const customCsv = localStorage.getItem('custom_past_questions_csv');
    const customExplanationCsv = localStorage.getItem('custom_past_explanations_csv');
    // In a real app, we would fetch different data based on `filter` (examId).
    
    // Check filter to determine which exam to return
    if (filter === '45') {
       // Allow custom CSV to override default 45th data if needed, or just use 45th
       questions = parsePastQuestions(customCsv || PAST_EXAM_45_CSV, customExplanationCsv || undefined);
    } else {
       // Default to 46th exam (Newest)
       questions = parsePastQuestions(PAST_EXAM_46_CSV, customExplanationCsv || undefined);
    }
    
    return questions;
  } else {
    const customCsv = localStorage.getItem('custom_predicted_questions_csv');
    const customExplanationCsv = localStorage.getItem('custom_predicted_explanations_csv');
    questions = parsePredictedQuestions(customCsv || INITIAL_PREDICTED_QUESTIONS_CSV, customExplanationCsv || undefined);
    
    if (filter && filter !== '全問') {
        return questions.filter(q => q.category.includes(filter));
    }
    return questions;
  }
};

